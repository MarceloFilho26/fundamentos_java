package exercicio_while_for;

public class exercicio06_2 {
	public static void main(String[] args) {
		for(int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		for(int i = 10; i >= 1; i--) {
			System.out.print(i + " ");
		}
	}
}
